export function computeAnxietyResult(answers: number[]): AnxietyResult {
  const primaryTypes = ['panic_cycle', 'body_hyperfocus', 'fear_of_recurrence', 'background_tension'] as const;

  const scores: Record<AnxietyType, number> = {
    panic_cycle: 0,
    body_hyperfocus: 0,
    fear_of_recurrence: 0,
    background_tension: 0,
    combined_type: 0,
  };

  QUIZ_QUESTIONS.forEach((q, i) => {
    const raw = answers[i];
    const answer = raw === undefined || raw === -1 ? 0 : raw;
    primaryTypes.forEach((type) => {
      scores[type] += answer * q.weights[type];
    });
  });

  const sorted = [...primaryTypes].sort((a, b) => scores[b] - scores[a]);
  const top1 = sorted[0];
  const top2 = sorted[1];

  const dominantType: AnxietyType =
    scores[top1] - scores[top2] <= 2 ? 'combined_type' : top1;

  return { ...ANXIETY_RESULTS[dominantType], type: dominantType, scores };
}
